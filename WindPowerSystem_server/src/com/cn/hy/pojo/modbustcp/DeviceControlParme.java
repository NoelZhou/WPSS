package com.cn.hy.pojo.modbustcp;

public class DeviceControlParme {
	private int id;
	private String name;
	private String bit_id;
	private String code;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getBit_id() {
		return bit_id;
	}

	public void setBit_id(String bit_id) {
		this.bit_id = bit_id;
	}
}
